    public void online_users(DefaultListModel dlm) {
        // online olan kullanıcıların listelenmesi   
        String selected = my_user_list.getSelectedValue();
        my_user_list.setModel(dlm);
        boolean isChanged = true;
        if (selected != null) {
            for (int i = 0; i < dlm.size(); i++) {
                String uname = (String) dlm.get(i);

                if (selected.equals(uname)) {
                    my_user_list.setSelectedIndex(i);  //Liste güncellenirken,sohbet edilen kişi online ise set eder
                    isChanged = false;
                    break;
                }
            }
        }
        if (isChanged) {
            //Liste güncellerinirken, mesajlaşılan kişi offline olursa listedeki ilk kişi set olur
            my_user_list.setSelectedIndex(0);
        }
        
        new_priv_chat(dlm); //priv chat fonksiyonu çağılılır
       
    }
    

       public void new_priv_chat(DefaultListModel dlm) {
        //eğer yeni biri geldirse, içi boş sohbet olustur
        String username = (String) dlm.get(dlm.getSize() - 1);
        if (!myPrivateChats.containsKey(username)) {
            myPrivateChats.put(username, "");
        }

    }
        public void priv_rec_msg(PrivateMsg pmsg) {
      
       
        String oldMsgs = "";
        if (myPrivateChats.get(pmsg.getSender()) != null) { //gelen mesajım null değilse eğer
            oldMsgs = myPrivateChats.get(pmsg.getSender()); //sohbete yazılır
        }
   // seçilen kişiyle sohbette dlm güncelle
        myPrivateChats.put(pmsg.getSender(), oldMsgs + pmsg.getSender() + ": " + pmsg.getContent() + "\n");
        if (pmsg.getSender().equals(my_user_list.getSelectedValue())) {
            prive_screen.setText(myPrivateChats.get(pmsg.getSender()));
        }

    }
        




