    private void btn_send_privateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_send_privateActionPerformed
      
        // Server uzerinden, listeden secilen kullaniciya, ozel mesaj gonderir
        // prive_msg nesnesi
        String senderUsername = jLabel4.getText();
        String targetUsername = my_user_list.getSelectedValue();
        String content = txt_priv.getText();
        PrivateMsg pmsg = new PrivateMsg(senderUsername, targetUsername, content);
        String oldMsgs = "";
        if (myPrivateChats.get(pmsg.getTarget()) != null) {
            oldMsgs = myPrivateChats.get(targetUsername);
        }

        myPrivateChats.put(targetUsername, oldMsgs + senderUsername + ": " + content + "\n");
        prive_screen.setText(myPrivateChats.get(targetUsername));
        txt_priv.setText("");

        Message msg = new Message(Message.Message_Type.prive_msg);
        msg.content = pmsg;
        Client.Send(msg);

    }//GEN-LAST:event_btn_send_privateActionPerformed

    private void send_fileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_send_fileActionPerformed

/*** file kısmını internetten araştırıp bularak yatim**/

        JFileChooser fileChooser = new JFileChooser();
        int selection = fileChooser.showOpenDialog(this);

        File file = null;
        // keep selected file from chooser in file variable
        if (selection == JFileChooser.APPROVE_OPTION)
        file = fileChooser.getSelectedFile();

        byte fileContent[] = new byte[(int)file.length()];
        try {
            BufferedInputStream bis = new BufferedInputStream(new FileInputStream(file));
            bis.read(fileContent, 0, fileContent.length);
            Message fileMSG = new Message(Message.Message_Type.File_t);
            fileMSG.file_cont = fileContent;
            fileMSG.content = file.getName(); // msg.content == file name
            Client.Send(fileMSG);
            System.out.println("File path : "+file.getAbsolutePath());
 // notifying to message sended by who
          String file_send =  jLabel4.getText();
 
            Message notifyFileMSG = new Message(Message.Message_Type.File_Notify);
            notifyFileMSG.content = file_send.toUpperCase() + ": " + file.getName() + " shared";
            Client.Send(notifyFileMSG);

        } catch (FileNotFoundException ex) {
            System.out.println("File dont found! "+ex);
        } catch (IOException ex) {
            System.out.println("File read exception'"+ex);
        }
        
    }//GEN-LAST:event_send_fileActionPerformed

